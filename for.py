print("range(10)")
for i in range (10):
    print(i)

    print("range(1,10)")
    for i in range(1,10):
        print(i)

        print("range(1,10,1)")
        for i in range(1,10,1):
            print(i)

            print("range(10,1,-1)")
            for i in range(10,1,-1):
                print(i)